package Forloop;

public class Forloop06 {
    public static void main(String[] args) {

        //Nested if - loop under loop

         for (int i =0; i<3; i++){
             for (int j=0; j<3 ;j++){
                 System.out.println(i + " " + j);
             }
         }
    }
}
// i=0 j=0,1,2
//i=1 , j=0,1,2
